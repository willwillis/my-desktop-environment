DOWNLOAD_DIR="imagemagick_scripts"

# Create the download directory if it doesn't exist
mkdir -p "$DOWNLOAD_DIR"

# Loop through the list of script names
for script_name in "${script_names[@]}"; do
  echo "Attempting to download script: $script_name"
  DOWNLOAD_URL="$BASE_URL/$DOWNLOAD_SCRIPT?scriptname=$script_name&dirname=$script_name"
  OUTPUT_FILE="$DOWNLOAD_DIR/$script_name"

  # Use wget to follow redirects and save the output
  wget -O "$OUTPUT_FILE" "$DOWNLOAD_URL"

  if [ $? -eq 0 ]; then
    echo "Successfully downloaded: $script_name"
  else
    echo "Failed to download: $script_name"
  fi
  echo "" # Add a newline for better readability
done

